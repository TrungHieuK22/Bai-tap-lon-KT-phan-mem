    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String tim = jTextField7.getText();
        if (tim.equals("")) {
            JOptionPane.showMessageDialog(this, "Mời bạn nhập mã sinh viên!");
        } else {
            try {
                Connection conn = Connect.openConnection();
                jTable3.setVisible(true);
                tbdk = (DefaultTableModel) jTable3.getModel();
                tbdk.setRowCount(0);
                String sql = "SELECT * FROM dangky WHERE MaSinhVien=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, tim);
                ResultSet rs = pst.executeQuery();
                ResultSetMetaData meta = rs.getMetaData();
                int number = meta.getColumnCount();
                int k = 1;
                while (rs.next()) {
                    Vector row = new Vector();
                    row.addElement(String.valueOf(k));
                    row.addElement(rs.getString(4));
                    row.addElement(rs.getString(5));
                    k++;
                    tbdk.addRow(row);
                    jTable3.setModel(tbdk);
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

