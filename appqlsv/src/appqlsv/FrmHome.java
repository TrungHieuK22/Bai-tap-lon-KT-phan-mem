    public void loadData() {
        try {
            Connection conn = Connect.openConnection();
            tbn = (DefaultTableModel) jTable1.getModel();
            tbn.setRowCount(0);
            String sql = "SELECT * FROM sinhvien";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData meta = rs.getMetaData();
            int number = meta.getColumnCount();
            int k = 1;
            while (rs.next()) {
                Vector row = new Vector();
                row.addElement(String.valueOf(k));
                for (int i = 1; i <= number; i++) {
                    row.addElement(rs.getString(i));
                }
                k++;
                tbn.addRow(row);
                jTable1.setModel(tbn);
            }
            jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (jTable1.getSelectedRow() >= 0) {
                        jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1) + "");
                        jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2) + "");
                        if (jTable1.getValueAt(jTable1.getSelectedRow(), 3).equals("Nam")) {
                            jRadioButton1.doClick();
                            
                        } else {
                            jRadioButton2.doClick();
                            
                        }
                        jTextField5.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 4) + "");
                        jTextField3.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 5) + "");
                        jTextField4.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 6) + "");
                    }
                }
            });
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

